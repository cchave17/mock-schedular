    rows = []
    for s in datas:
        name = next(iter(s))
        row_stud_schedule = [name]
        for sched in s[name]:
            row_stud_schedule.append(sched["Rotation"])
        rows.append(tuple(row_stud_schedule))
    rows = tuple(rows)
    print(rows)


